    private void txt_buscarporcodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_buscarporcodigoActionPerformed
        int codigo = Integer.parseInt(txt_codigobuscado.getText());
        ControladorProducto cl = new ControladorProducto();
        Producto prod = cl.buscarProductoPorCodigo(codigo);


        DefaultTableModel modeloTabla = (DefaultTableModel) tbl_productos.getModel();
        modeloTabla.setRowCount(0);

        if (String.valueOf(prod.getCodigo())!=null)
        {
            String descripcion = prod.getDescripcion();
            int precio = prod.getPrecio();
            int stock_minimo = prod.getStockMinimo();
            int stock_actual = prod.getStockActual();
            String tipo_producto = prod.getTipoProducto();

            modeloTabla.addRow (new Object[]{codigo, descripcion, precio, stock_minimo, stock_actual, tipo_producto});


        }       
        
    }//GEN-LAST:event_txt_buscarporcodigoActionPerformed

