    private void btn_mostrartodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrartodoActionPerformed
        // TODO add your handling code here:String tituloBuscado = txt_ingresarLibro.getText();
        
        String codigoBuscado = txt_codigobuscado.getText();
        //int codigoBuscado = Integer.parseInt(txt_codigobuscado.getText());
        ControladorProducto cl = new ControladorProducto();
        
        if(codigoBuscado.isEmpty())
        {
        
            ArrayList<Producto> productos = cl.traerTodo();
           
            DefaultTableModel tablaProductos = (DefaultTableModel) tbl_productos.getModel();
            tablaProductos.setRowCount(0); 
            
            for(Producto l : productos)
            {
            
                int codigo = l.getCodigo();
                String descripcion = l.getDescripcion();
                int precio = l.getPrecio();
                int stockMinimo = l.getStockMinimo();
                int stockActual = l.getStockActual();
                String tipoProducto = l.getTipoProducto();
                
                
                
                  tablaProductos.addRow(new Object[]{codigo,descripcion,precio,stockMinimo,stockActual,tipoProducto});
            }
            
        }
    }//GEN-LAST:event_btn_mostrartodoActionPerformed

